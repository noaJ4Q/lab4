package com.example.laboratory4.Objetos;

public class Geolocation {
    public String name;
    public double lat;
    public double lon;
    public String country;
    public String state;

    public String getName() {
        return name;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }

    public String getCountry() {
        return country;
    }

    public String getState() {
        return state;
    }
}
